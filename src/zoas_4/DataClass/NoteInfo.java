package zoas_4.DataClass;

public class NoteInfo {
	private String name;
    private String creatDate;
    private String editDate;
    private String recordText;
    private String summaryTest;

    public String getName() { return name; }
    public void setName(String value) { this.name = value; }

    public String getCreatDate() { return creatDate; }
    public void setCreatDate(String value) { this.creatDate = value; }

    public String getEditDate() { return editDate; }
    public void setEditDate(String value) { this.editDate = value; }

    public String getRecordText() { return recordText; }
    public void setRecordText(String value) { this.recordText = value; }

    public String getSummaryText() { return summaryTest; }
    public void setSummaryText(String value) { this.summaryTest = value; }

}
